def get_same_genre_choices(genre: str, exclude: list[str]) -> tuple[str, str]:
    """
    同じジャンルから、excludeに含まれない別の2候補を返す。
    候補が足りない場合は重複を許容する。
    """
    candidates = [t for t in THEMES[genre] if t not in exclude]
    if len(candidates) >= 2:
        return tuple(random.sample(candidates, 2))
    elif len(candidates) == 1:
        return candidates[0], random.choice(THEMES[genre])
    else:
        return tuple(random.sample(THEMES[genre], 2))


def get_random_choices() -> tuple[str, str, str]:
    """
    ランダムにジャンルを選び、そのジャンルから2つの候補を返す。
    戻り値: (ジャンル名, 選択肢A, 選択肢B)
    """
    genre = random.choice(list(THEMES.keys()))
    choices = random.sample(THEMES[genre], 2)
    return genre, choices[0], choices[1]


def build_opening_prompt() -> tuple[str, str, str]:
    """
    最初の質問文と2択を生成する。
    戻り値: (TTS用テキスト, 選択肢A, 選択肢B)
    """
    genre, a, b = get_random_choices()
    text = f"きょうは なんの おはなしにする？{a}と {b}、どっちでもいいよ！"
    return text, a, b
